class PrimitiveDataTypeExample{

     public static void main(String[] arg){
	 
	 
	   //int datatype
	   
	   int  age = 10;
	   
	   System.out.println(age);
	   
	   //byte datatype
	   byte n = 1;
	   System.out.println(n);
	   
	   //short datatype
	   short  s = 11;
	   System.out.println(s);
	   
	   //long datatype
	   
	   long m = 23232;
	   System.out.println(m);
	   
	      }
	   }
	   
	   
	   